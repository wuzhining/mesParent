package com.techsoft.entity.firm;

import java.util.Date;

import com.techsoft.entity.common.FirmEmployee;

public class FirmEmployeeParamVo extends FirmEmployee {
	private static final long serialVersionUID = 1652123805817624466L;


	public FirmEmployeeParamVo (){
	}
	
	public FirmEmployeeParamVo(FirmEmployee value) {
		value.cloneProperties(this);
	}	
	
	private Date createTimeBegin;
	private Date createTimeEnd;
	private Date modifyTimeBegin;
	private Date modifyTimeEnd;	
	private String realNameLike;
	private String employeeCodeLike;
	private String nativeplaceLike;
	
	
	public Date getCreateTimeBegin() {
		return createTimeBegin;
	}

	public void setCreateTimeBegin(Date value) {
		this.createTimeBegin = value;
	}

	public Date getCreateTimeEnd() {
		return createTimeEnd;
	}

	public void setCreateTimeEnd(Date value) {
		this.createTimeEnd = value;
	}

	public Date getModifyTimeBegin() {
		return modifyTimeBegin;
	}

	public void setModifyTimeBegin(Date value) {
		this.modifyTimeBegin = value;
	}

	public Date getModifyTimeEnd() {
		return modifyTimeEnd;
	}

	public void setModifyTimeEnd(Date value) {
		this.modifyTimeEnd = value;
	}

	public String getRealNameLike() {
		return realNameLike;
	}

	public void setRealNameLike(String realNameLike) {
		this.realNameLike = realNameLike;
	}

	public String getEmployeeCodeLike() {
		return employeeCodeLike;
	}

	public void setEmployeeCodeLike(String employeeCodeLike) {
		this.employeeCodeLike = employeeCodeLike;
	}

	public String getNativeplaceLike() {
		return nativeplaceLike;
	}

	public void setNativeplaceLike(String nativeplaceLike) {
		this.nativeplaceLike = nativeplaceLike;
	}
	
}
