package com.techsoft.entity.common;

import com.techsoft.common.BaseEntity;

public class BillInventoryItem extends BaseEntity {
	private static final long serialVersionUID = 5487838084697642923L;


	public BillInventoryItem(){	
	}
	
	private Long tenantId;	
	   
	private Long billId;	
	   
	private Long productId;	
	   
	private Long materialId;	
	   
	private Double quantity;	
	   
	private Double quantityStock;	
	   
	private String remark;	
	   
                		
	public Long getTenantId() {
		return tenantId;
	}

	public void setTenantId(Long tenantId) {
		this.tenantId = tenantId;
	}
                		
	public Long getBillId() {
		return billId;
	}

	public void setBillId(Long billId) {
		this.billId = billId;
	}
                		
	public Long getProductId() {
		return productId;
	}

	public void setProductId(Long productId) {
		this.productId = productId;
	}
                		
	public Long getMaterialId() {
		return materialId;
	}

	public void setMaterialId(Long materialId) {
		this.materialId = materialId;
	}
                		
	public Double getQuantity() {
		return quantity;
	}

	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}
                		
	public Double getQuantityStock() {
		return quantityStock;
	}

	public void setQuantityStock(Double quantityStock) {
		this.quantityStock = quantityStock;
	}
                		
	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
