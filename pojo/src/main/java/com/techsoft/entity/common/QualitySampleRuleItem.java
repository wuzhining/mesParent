package com.techsoft.entity.common;

import com.techsoft.common.BaseEntity;

public class QualitySampleRuleItem extends BaseEntity {
	private static final long serialVersionUID = -1983249612559065804L;


	public QualitySampleRuleItem(){	
	}
	
	private Long tenantId;	
	   
	private Long sampleRuleId;	
	   
	private String sampleLetter;	
	   
	private Integer quantity;	
	   
	private Integer quantityAc;	
	   
	private Integer quantityRe;	
	   
                		
	public Long getTenantId() {
		return tenantId;
	}

	public void setTenantId(Long tenantId) {
		this.tenantId = tenantId;
	}
                		
	public Long getSampleRuleId() {
		return sampleRuleId;
	}

	public void setSampleRuleId(Long sampleRuleId) {
		this.sampleRuleId = sampleRuleId;
	}
                		
	public String getSampleLetter() {
		return sampleLetter;
	}

	public void setSampleLetter(String sampleLetter) {
		this.sampleLetter = sampleLetter;
	}
                		
	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
                		
	public Integer getQuantityAc() {
		return quantityAc;
	}

	public void setQuantityAc(Integer quantityAc) {
		this.quantityAc = quantityAc;
	}
                		
	public Integer getQuantityRe() {
		return quantityRe;
	}

	public void setQuantityRe(Integer quantityRe) {
		this.quantityRe = quantityRe;
	}
}
