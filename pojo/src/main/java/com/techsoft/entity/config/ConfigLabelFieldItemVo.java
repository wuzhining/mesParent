package com.techsoft.entity.config;

import com.techsoft.entity.common.ConfigLabelField;
import com.techsoft.entity.common.ConfigLabelFieldItem;
import com.techsoft.entity.common.ConfigLabelFunction;

public class ConfigLabelFieldItemVo extends ConfigLabelFieldItem {
	private static final long serialVersionUID = -6941356662124285086L;
	private ConfigLabelField configLabelField;
	private ConfigLabelFunction configLabelFunction;
	private String labelFunctionName;
	
	public ConfigLabelFieldItemVo() {	
	}
	
	public ConfigLabelFieldItemVo(ConfigLabelFieldItem value) {
		value.cloneProperties(this);
	}

	public ConfigLabelField getConfigLabelField() {
		return configLabelField;
	}

	public void setConfigLabelField(ConfigLabelField configLabelField) {
		this.configLabelField = configLabelField;
	}

	public ConfigLabelFunction getConfigLabelFunction() {
		return configLabelFunction;
	}

	public void setConfigLabelFunction(ConfigLabelFunction configLabelFunction) {
		this.configLabelFunction = configLabelFunction;
	}

	public String getLabelFunctionName() {
		return labelFunctionName;
	}

	public void setLabelFunctionName(String labelFunctionName) {
		this.labelFunctionName = labelFunctionName;
	}	
	
}
