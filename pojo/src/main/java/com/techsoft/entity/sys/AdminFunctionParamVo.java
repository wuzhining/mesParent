package com.techsoft.entity.sys;

import java.util.Date;

import com.techsoft.entity.common.AdminFunction;

public class AdminFunctionParamVo extends AdminFunction {
	private static final long serialVersionUID = 7193112050919375723L;


	public AdminFunctionParamVo (){
	}
	
	public AdminFunctionParamVo(AdminFunction value) {
		value.cloneProperties(this);
	}	
	
	private Date createTimeBegin;
	private Date createTimeEnd;
	private Date modifyTimeBegin;
	private Date modifyTimeEnd;	
	
	private String likeFunctionName;
	public Date getCreateTimeBegin() {
		return createTimeBegin;
	}

	public void setCreateTimeBegin(Date value) {
		this.createTimeBegin = value;
	}

	public Date getCreateTimeEnd() {
		return createTimeEnd;
	}

	public void setCreateTimeEnd(Date value) {
		this.createTimeEnd = value;
	}

	public Date getModifyTimeBegin() {
		return modifyTimeBegin;
	}

	public void setModifyTimeBegin(Date value) {
		this.modifyTimeBegin = value;
	}

	public Date getModifyTimeEnd() {
		return modifyTimeEnd;
	}

	public void setModifyTimeEnd(Date value) {
		this.modifyTimeEnd = value;
	}

	public String getLikeFunctionName() {
		return likeFunctionName;
	}

	public void setLikeFunctionName(String likeFunctionName) {
		this.likeFunctionName = likeFunctionName;
	}		
	
}
