package com.techsoft.entity.common;

import com.techsoft.common.BaseEntity;

public class FirmPost extends BaseEntity {
	private static final long serialVersionUID = -5657654052621453770L;


	public FirmPost(){	
	}
	
	private Long tenantId;	
	   
	private Long departmenId;	
	   
	private String postName;	
	   
	private String postCode;	
	   
	private Long parentId;	
	   
	private String isValid;	
	   
	private String remark;	
	   
                		
	public Long getTenantId() {
		return tenantId;
	}

	public void setTenantId(Long tenantId) {
		this.tenantId = tenantId;
	}
                		
	public Long getDepartmenId() {
		return departmenId;
	}

	public void setDepartmenId(Long departmenId) {
		this.departmenId = departmenId;
	}
                		
	public String getPostName() {
		return postName;
	}

	public void setPostName(String postName) {
		this.postName = postName;
	}
                		
	public String getPostCode() {
		return postCode;
	}

	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
                		
	public Long getParentId() {
		return parentId;
	}

	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}
                		
	public String getIsValid() {
		return isValid;
	}

	public void setIsValid(String isValid) {
		this.isValid = isValid;
	}
                		
	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
