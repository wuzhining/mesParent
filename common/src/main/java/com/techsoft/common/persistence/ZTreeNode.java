package com.techsoft.common.persistence;

import java.util.List;

public class ZTreeNode {
	private Long id;
	private Long parentId;
	private String name;
	private Boolean isParentId;
	private Boolean open;
	private List<ZTreeNode> children;
	
	public ZTreeNode() {
		
	}

	public ZTreeNode(Long id, Long parentId, String name, Boolean isParentId, Boolean open, List<ZTreeNode> children) {
		super();
		this.id = id;
		this.parentId = parentId;
		this.name = name;
		this.isParentId = isParentId;
		this.open = open;
		this.children = children;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getParentId() {
		return parentId;
	}

	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Boolean getIsParentId() {
		return isParentId;
	}

	public void setIsParentId(Boolean isParentId) {
		this.isParentId = isParentId;
	}

	public Boolean getOpen() {
		return open;
	}

	public void setOpen(Boolean open) {
		this.open = open;
	}

	public List<ZTreeNode> getChildren() {
		return children;
	}

	public void setChildren(List<ZTreeNode> children) {
		this.children = children;
	}
	
	
}
