package com.techsoft.entity.firm;

import java.util.Date;

import com.techsoft.entity.common.FirmDepartment;

public class FirmDepartmentParamVo extends FirmDepartment {
	private static final long serialVersionUID = 6939407952023901173L;


	public FirmDepartmentParamVo (){
	}
	
	public FirmDepartmentParamVo(FirmDepartment value) {
		value.cloneProperties(this);
	}
	private String departmentNameLike;
	private String departmentCodeLike;
	private Integer parent;
	private Date createTimeBegin;
	private Date createTimeEnd;
	private Date modifyTimeBegin;
	private Date modifyTimeEnd;	
	
	
	public Date getCreateTimeBegin() {
		return createTimeBegin;
	}

	public void setCreateTimeBegin(Date value) {
		this.createTimeBegin = value;
	}

	public Date getCreateTimeEnd() {
		return createTimeEnd;
	}

	public void setCreateTimeEnd(Date value) {
		this.createTimeEnd = value;
	}

	public Date getModifyTimeBegin() {
		return modifyTimeBegin;
	}

	public void setModifyTimeBegin(Date value) {
		this.modifyTimeBegin = value;
	}

	public Date getModifyTimeEnd() {
		return modifyTimeEnd;
	}

	public void setModifyTimeEnd(Date value) {
		this.modifyTimeEnd = value;
	}

	public Integer getParent() {
		return parent;
	}

	public void setParent(Integer parent) {
		this.parent = parent;
	}

	public String getDepartmentNameLike() {
		return departmentNameLike;
	}

	public void setDepartmentNameLike(String departmentNameLike) {
		this.departmentNameLike = departmentNameLike;
	}

	public String getDepartmentCodeLike() {
		return departmentCodeLike;
	}

	public void setDepartmentCodeLike(String departmentCodeLike) {
		this.departmentCodeLike = departmentCodeLike;
	}
	
	
	
}
