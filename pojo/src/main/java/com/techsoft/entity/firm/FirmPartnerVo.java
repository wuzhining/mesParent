package com.techsoft.entity.firm;

import com.techsoft.entity.common.ConfigArea;
import com.techsoft.entity.common.ConfigCountry;
import com.techsoft.entity.common.ConfigDictionary;
import com.techsoft.entity.common.FirmPartner;

public class FirmPartnerVo extends FirmPartner {
	private static final long serialVersionUID = -1117540660828875728L;
	private ConfigDictionary configDictionary;//数据字典
	private ConfigCountry configCountry;
	private ConfigArea configAreaP;
	private ConfigArea configAreaC;
	private ConfigArea configArea;
	
	public FirmPartnerVo() {	
	}
	
	public FirmPartnerVo(FirmPartner value) {
		value.cloneProperties(this);
	}

	public ConfigDictionary getConfigDictionary() {
		return configDictionary;
	}

	public void setConfigDictionary(ConfigDictionary configDictionary) {
		this.configDictionary = configDictionary;
	}

	public ConfigCountry getConfigCountry() {
		return configCountry;
	}

	public void setConfigCountry(ConfigCountry configCountry) {
		this.configCountry = configCountry;
	}

	public ConfigArea getConfigAreaP() {
		return configAreaP;
	}

	public void setConfigAreaP(ConfigArea configAreaP) {
		this.configAreaP = configAreaP;
	}

	public ConfigArea getConfigAreaC() {
		return configAreaC;
	}

	public void setConfigAreaC(ConfigArea configAreaC) {
		this.configAreaC = configAreaC;
	}

	public ConfigArea getConfigArea() {
		return configArea;
	}

	public void setConfigArea(ConfigArea configArea) {
		this.configArea = configArea;
	}	
	
}
